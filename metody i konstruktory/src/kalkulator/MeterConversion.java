package kalkulator;

public class MeterConversion {

    double meterToCenti(double meter) {
          return meter * 100;
    }

    double meterToMilli(double milli) {
           return milli * 1000;;
    }

    double centiToMeter(double centi) {
           return centi / 100;
    }

    double milliToMeters(double milli) {
        return milli / 1000;
    }
}
