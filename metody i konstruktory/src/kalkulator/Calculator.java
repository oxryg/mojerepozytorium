package kalkulator;

public class Calculator {

    void add(double x, double y) {
        System.out.println(x + y);
    }

    void subtract(double x, double y) {
        System.out.println(x - y);
    }

    void multiply(double x, double y) {
        System.out.println(x * y);
    }

    void divide(double x, double y) {
        System.out.println(x / y);
    }
}

